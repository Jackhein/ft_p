#include "libft.h"

void	ft_tabdel(void ***tab)
{
	int	i;

	i = -1;
	if (!tab)
		return ;
	while (*tab[++i])
		ft_memdel(tab[i]);
	free(*tab);
	*tab = NULL;
}
